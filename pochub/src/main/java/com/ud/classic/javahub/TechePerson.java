package com.ud.classic.javahub;

public class TechePerson {
	
	private int javaScore;
	private int mlScore ;
	private int apacheSparkScore;
	private int apacheKafkaScore;
	
	private String name;
	private String country;
	
	
	
	public int getJavaScore() {
		return javaScore;
	}
	public void setJavaScore(int javaScore) {
		this.javaScore = javaScore;
	}
	public int getMlScore() {
		return mlScore;
	}
	public void setMlScore(int mlScore) {
		this.mlScore = mlScore;
	}
	public int getApacheSparkScore() {
		return apacheSparkScore;
	}
	public void setApacheSparkScore(int apacheSparkScore) {
		this.apacheSparkScore = apacheSparkScore;
	}
	public int getApacheKafkaScore() {
		return apacheKafkaScore;
	}
	public void setApacheKafkaScore(int apacheKafkaScore) {
		this.apacheKafkaScore = apacheKafkaScore;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	

}
